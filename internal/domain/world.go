// FileEntry represents a file in the virtual filesystem.
type FileEntry struct {
	Content     string
	Permissions string
	Owner       string
	Group       string
	Size        int
	Mtime       string
}

func NewFileEntry(content string) *FileEntry {
	return &FileEntry{
		Content:     content,
		Permissions: "-rw-r--r--",
		Owner:       "root",
		Group:       "root",
		Size:        len(content),
		Mtime:       time.Now().UTC().Format("2006-01-02 15:04"),
	}
}

func NewDirEntry(owner string) *FileEntry {
	if owner == "" {
		owner = "root"
	}
	return &FileEntry{
		Content:     "",
		Permissions: "drwxr-xr-x",
		Owner:       owner,
		Group:       owner,
		Size:        4096,
		Mtime:       time.Now().UTC().Format("2006-01-02 15:04"),
	}
}

// CachedRule stores an LLM-generated response for consistent replay.
type CachedRule struct {
	Key       string
	Command   string // ls, cat, find, grep, etc.
	Path      string // target path
	Flags     string // command flags
	Output    string // cached LLM output
	CreatedAt time.Time
	HitCount  int
}

// DynamicRuleCache stores LLM-generated responses so repeated commands
// return identical output. When the attacker modifies the filesystem,
// affected entries are invalidated and re-generated on next access.
type DynamicRuleCache struct {
	rules map[string]*CachedRule
	mu    sync.RWMutex
}

func NewDynamicRuleCache() *DynamicRuleCache {
	return &DynamicRuleCache{rules: make(map[string]*CachedRule)}
}

// CacheKey builds a normalized cache key from command components.
func CacheKey(command, flags, targetPath string) string {
	flags = strings.TrimSpace(flags)
	if flags != "" {
		return command + ":" + flags + ":" + targetPath
	}
	return command + ":" + targetPath
}

// Lookup returns a cached rule or nil.
func (c *DynamicRuleCache) Lookup(key string) *CachedRule {
	c.mu.RLock()
	defer c.mu.RUnlock()
	r, ok := c.rules[key]
	if !ok {
		return nil
	}
	r.HitCount++
	return r
}

// Store saves an LLM response in the cache.
func (c *DynamicRuleCache) Store(key, command, targetPath, flags, output string) {
	c.mu.Lock()
	defer c.mu.Unlock()
	c.rules[key] = &CachedRule{
		Key:       key,
		Command:   command,
		Path:      targetPath,
		Flags:     flags,
		Output:    output,
		CreatedAt: time.Now(),
	}
}

// InvalidateDir removes all cached rules whose Path equals or is a
// child of dir. This is called when the attacker writes to the filesystem.
func (c *DynamicRuleCache) InvalidateDir(dir string) {
	dir = path.Clean(dir)
	c.mu.Lock()
	defer c.mu.Unlock()
	for key, r := range c.rules {
		rp := path.Clean(r.Path)
		if rp == dir || strings.HasPrefix(rp, dir+"/") {
			delete(c.rules, key)
		}
	}
}

// InvalidateFile removes cached rules for a specific file path (cat cache).
func (c *DynamicRuleCache) InvalidateFile(filepath string) {
	filepath = path.Clean(filepath)
	c.mu.Lock()
	defer c.mu.Unlock()
	for key, r := range c.rules {
		if r.Command == "cat" && path.Clean(r.Path) == filepath {
			delete(c.rules, key)
		}
	}
}

// WorldState is the virtual filesystem for a session.
type WorldState struct {
	files map[string]*FileEntry
	cache *DynamicRuleCache
}

// NewWorldState creates a world state with default files.
func NewWorldState() *WorldState {
	w := &WorldState{
		files: make(map[string]*FileEntry),
		cache: NewDynamicRuleCache(),
	}
	w.initDefaults()
	return w
}

// NewWorldStateForHost creates a world state for a remote host.
// Only critical attack-chain clue files are pre-seeded; all other content
// is generated by the LLM fallback on demand.
func NewWorldStateForHost(hostname string) *WorldState {
	w := &WorldState{
		files: make(map[string]*FileEntry),
		cache: NewDynamicRuleCache(),
	}
	w.initHostStructure(hostname)
	w.initHostClueFiles(hostname)
	return w
}

