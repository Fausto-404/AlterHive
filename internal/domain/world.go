// IsDir checks if a path is a directory.
func (w *WorldState) IsDir(filepath string) bool {
	if f, ok := w.files[filepath]; ok {
		return f.Permissions[0] == 'd'
	}
	return false
}

// ListFiles returns sorted filenames in a directory.
func (w *WorldState) ListFiles(dir string) []string {
	dir = path.Clean(dir)
	var names []string
	for fp := range w.files {
		parent := path.Dir(fp)
		if parent == dir && fp != dir {
			name := path.Base(fp)
			names = append(names, name)
		}
	}
	sort.Strings(names)
	return names
}

// GetDirectoryListing returns ls -la style entries.
func (w *WorldState) GetDirectoryListing(dir string) []map[string]string {
	dir = path.Clean(dir)
	var entries []map[string]string

	// Add subdirectories (both direct entries and implicit dirs from deeper paths)
	subdirs := make(map[string]bool)
	for fp := range w.files {
		if !strings.HasPrefix(fp, dir+"/") || fp == dir {
			continue
		}
		rel := strings.TrimPrefix(fp, dir+"/")
		if idx := strings.Index(rel, "/"); idx > 0 {
			subdirs[rel[:idx]] = true
		}
	}

	// Also add explicit directory entries
	for fp, f := range w.files {
		if path.Dir(fp) != dir || fp == dir {
			continue
		}
		if f.Permissions[0] == 'd' {
			subdirs[path.Base(fp)] = true
		}
	}

	now := time.Now().UTC().Format("2006-01-02 15:04")
	for name := range subdirs {
		entries = append(entries, map[string]string{
			"permissions": "drwxr-xr-x",
			"links":       "2",
			"owner":       "root",
			"group":       "root",
			"size":        "4096",
			"mtime":       now,
			"name":        name,
		})
	}

	// Add files
	for fp, f := range w.files {
		if path.Dir(fp) != dir || fp == dir {
			continue
		}
		if f.Permissions[0] == 'd' {
			continue // already added above
		}
		entries = append(entries, map[string]string{
			"permissions": f.Permissions,
			"links":       "1",
			"owner":       f.Owner,
			"group":       f.Group,
			"size":        fmt.Sprintf("%d", f.Size),
			"mtime":       f.Mtime,
			"name":        path.Base(fp),
		})
	}

	sort.Slice(entries, func(i, j int) bool {
		return entries[i]["name"] < entries[j]["name"]
	})
	return entries
}

