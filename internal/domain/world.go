// RebaseCIDR rewrites seeded /24 clue data to the active primary topology CIDR.
func (w *WorldState) RebaseCIDR(oldCIDR, newCIDR string) {
	oldPrefix := cidr24Prefix(oldCIDR)
	newPrefix := cidr24Prefix(newCIDR)
	if oldPrefix == "" || newPrefix == "" || oldPrefix == newPrefix {
		return
	}
	for _, entry := range w.files {
		if entry == nil || entry.Content == "" {
			continue
		}
		entry.Content = strings.ReplaceAll(entry.Content, oldPrefix+".", newPrefix+".")
		entry.Size = len(entry.Content)
	}
}

func cidr24Prefix(cidr string) string {
	if !strings.HasSuffix(cidr, "/24") {
		return ""
	}
	base := strings.TrimSuffix(cidr, "/24")
	parts := strings.Split(base, ".")
	if len(parts) != 4 {
		return ""
	}
	return strings.Join(parts[:3], ".")
}

